const uint8_t* get_glyph(uint32_t codepoint) {
  if (codepoint == 0xFE0F) { // variation selector-16 (emoji style)
    return NULL;
  }
  auto it = font_map.find(codepoint);
  if (it != font_map.end()) {
    return it->second.data();
  }

  Serial.printf("Unknown codepoint: 0x%X\n", codepoint);
  return font_map.at(0x00).data();
}
